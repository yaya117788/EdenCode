x = int(input())

if 0 <= x <= 100 :
    if 90 <= x <= 100 :
        print('A')
    else :
        if 80 <= x <= 89 :
            print('B')
        else :
            if 70 <= x <= 79 :
                print('C')
            else :
                if 60 <= x <= 69 :
                    print('D')
                else :
                    print('F')

